package buaa.define.Node;

public class TMAX {
	public int tit ;
	public int art ;
	public TMAX(int tit,int art){
		this.tit = tit ;
		this.art = art ;
	}
	public void setTit(int tit){
		this.tit = tit;
	}
	public void setArt(int art){
		this.art = art;
	}
	public int getTit(){
		return tit;
	}
	public int getArt(){
		return art;
	}
}
